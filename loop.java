public class Loop
{
    public static void main(String[] args)
    {
      int i=0;
      while(i<=5) // while loop
      {
        System.out.println("Hello");
        int j=1;
        while(j<5)
        {
            System.out.println("Jii");
            j++;
        }
        i++;
      }
      for(int i=0 ;i<5;i++)//for loop
      {
        System.out.println(i);
      }


    }
}